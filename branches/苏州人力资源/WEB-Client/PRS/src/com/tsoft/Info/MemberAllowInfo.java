package com.tsoft.Info;

public class MemberAllowInfo {
	
	private String allowDate;
	private double allowAmt;
	private int recNum;
	private int teamNum;
	public String getAllowDate() {
		return allowDate;
	}
	public void setAllowDate(String allowDate) {
		this.allowDate = allowDate;
	}
	public double getAllowAmt() {
		return allowAmt;
	}
	public void setAllowAmt(double allowAmt) {
		this.allowAmt = allowAmt;
	}
	public int getRecNum() {
		return recNum;
	}
	public void setRecNum(int recNum) {
		this.recNum = recNum;
	}
	public int getTeamNum() {
		return teamNum;
	}
	public void setTeamNum(int teamNum) {
		this.teamNum = teamNum;
	}
	
	
}
